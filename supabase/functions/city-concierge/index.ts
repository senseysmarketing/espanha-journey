import { serve } from "https://deno.land/std@0.168.0/http/server.ts";

const corsHeaders = {
  "Access-Control-Allow-Origin": "*",
  "Access-Control-Allow-Headers": "authorization, x-client-info, apikey, content-type, x-supabase-client-platform, x-supabase-client-platform-version, x-supabase-client-runtime, x-supabase-client-runtime-version",
};

serve(async (req) => {
  if (req.method === "OPTIONS") return new Response(null, { headers: corsHeaders });

  try {
    const { messages, budget, lifestyle } = await req.json();
    const LOVABLE_API_KEY = Deno.env.get("LOVABLE_API_KEY");
    if (!LOVABLE_API_KEY) throw new Error("LOVABLE_API_KEY is not configured");

    const systemPrompt = `Você é o Concierge de Destinos do Espanha Pass — um assistente especializado em ajudar imigrantes brasileiros a escolher a melhor cidade na Espanha para viver.

Suas recomendações devem considerar:
- Orçamento mensal do usuário (custo de vida, aluguel médio)
- Perfil: Trabalho Remoto, Família com filhos, Estudante universitário
- Comunidade brasileira local
- Facilidade burocrática (tempo de espera para NIE, Empadronamiento)
- Clima, segurança e qualidade de vida

Cidades que você conhece bem: Madrid, Barcelona, Valencia, Málaga, Sevilla, Bilbao, Alicante, Granada, Zaragoza.

Quando recomendar uma cidade, mencione-a pelo nome exato (ex: "Madrid", "Valencia", "Málaga") para que o app possa exibir o card informativo.

Responda sempre em português brasileiro, de forma acolhedora e prática. Use markdown para formatar.${budget ? `\n\nOrçamento informado: ${budget}€/mês` : ''}${lifestyle ? `\nPerfil: ${lifestyle}` : ''}`;

    const response = await fetch("https://ai.gateway.lovable.dev/v1/chat/completions", {
      method: "POST",
      headers: {
        Authorization: `Bearer ${LOVABLE_API_KEY}`,
        "Content-Type": "application/json",
      },
      body: JSON.stringify({
        model: "google/gemini-3-flash-preview",
        messages: [
          { role: "system", content: systemPrompt },
          ...messages,
        ],
        stream: true,
      }),
    });

    if (!response.ok) {
      if (response.status === 429) {
        return new Response(JSON.stringify({ error: "Limite de requisições excedido. Tente novamente em alguns segundos." }), {
          status: 429, headers: { ...corsHeaders, "Content-Type": "application/json" },
        });
      }
      if (response.status === 402) {
        return new Response(JSON.stringify({ error: "Créditos esgotados. Adicione fundos em Settings > Workspace > Usage." }), {
          status: 402, headers: { ...corsHeaders, "Content-Type": "application/json" },
        });
      }
      const t = await response.text();
      console.error("AI gateway error:", response.status, t);
      return new Response(JSON.stringify({ error: "Erro no serviço de IA" }), {
        status: 500, headers: { ...corsHeaders, "Content-Type": "application/json" },
      });
    }

    return new Response(response.body, {
      headers: { ...corsHeaders, "Content-Type": "text/event-stream" },
    });
  } catch (e) {
    console.error("city-concierge error:", e);
    return new Response(JSON.stringify({ error: e instanceof Error ? e.message : "Erro desconhecido" }), {
      status: 500, headers: { ...corsHeaders, "Content-Type": "application/json" },
    });
  }
});
